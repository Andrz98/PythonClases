# Plus and minus operators
plus = 3 + 5
minus = 9 - 2
print("Plus:", plus)
print("Minus:", minus)

# Multiplication and división operators
multiplication = 4 * 7
division = 8 / 2
print("Multiplication:", multiplication)
# When you use the division in Python.
# The result will always be a float and never an int.
print("Division:", division)

# Exponentiation (**)
exponent = 12 ** 5
print("Exponent:", exponent)

# Floor division (//)
# When you use the floor division in Python. The result will always be an int.
# The floor division rounds down (redonde hacía abajo).
floor_division = 12 // 5

# type function
type_of_date = type(floor_division)
print("Type of date is an:", type_of_date)

# modulo
modulo = 14 % 3
print("Modulo:", modulo)